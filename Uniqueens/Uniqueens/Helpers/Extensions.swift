        }
        
        static var customLightPink: Color {
            return Color("CustomLightPink")
        }
        
        static var customGray: Color {
            return Color("CustomGray")
        }
        
        static var customWhite: Color {
            return Color("CustomWhite")
        }
        
    }
}
